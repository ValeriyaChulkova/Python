x = int(input('Введите возраст Тани: '))
y = int(input('Введите возраст Мити: '))
avg_age = (x + y) / 2
tanya_deviation = x - avg_age
mitia_deviation = y - avg_age
print('Средний возраст равен:', avg_age)
print('Отклонение возраста Тани от среднего возраста равно:', tanya_deviation)
print('Отклонение возраста Мити от среднего возраста равно:', mitia_deviation)
