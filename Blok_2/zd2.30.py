# Задаем координаты вершин четырехугольника
x1 = float(input("Введите координату x1: "))
y1 = float(input("Введите координату y1: "))
x2 = float(input("Введите координату x2: "))
y2 = float(input("Введите координату y2: "))
x3 = float(input("Введите координату x3: "))
y3 = float(input("Введите координату y3: "))
x4 = float(input("Введите координату x4: "))
y4 = float(input("Введите координату y4: "))

# Вычисляем площадь каждого треугольника
s1 = abs((x2 - x1) * (y3 - y1) - (x3 - x1) * (y2 - y1)) / 2
s2 = abs((x3 - x2) * (y4 - y2) - (x4 - x2) * (y3 - y2)) / 2
s3 = abs((x4 - x3) * (y1 - y3) - (x1 - x3) * (y4 - y3)) / 2
s4 = abs((x1 - x4) * (y2 - y4) - (x2 - x4) * (y1 - y4)) / 2

# Вычисляем площадь четырехугольника как сумму площадей треугольников
s = s1 + s2 + s3 + s4

# Выводим результат
print("Площадь четырехугольника равна", s)